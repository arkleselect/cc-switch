def build_operation_section(title: str, idx_title: str) -> list[str]:
    body: list[str] = [h1(title)]
    if "登录与退出" in title:
        body.append(p("本章介绍系统访问入口、账号校验、密码处理和安全退出等基础能力，是所有用户使用系统前必须掌握的操作内容。"))
